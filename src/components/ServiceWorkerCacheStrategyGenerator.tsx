import { useState, useCallback } from 'preact/hooks';

type CacheStrategy = 'network-first' | 'cache-first' | 'stale-while-revalidate' | 'cache-only' | 'network-only';
type AgeUnit = 'seconds' | 'minutes' | 'hours' | 'days';

interface RouteRule {
  id: string;
  pattern: string;
  strategy: CacheStrategy;
  cacheName: string;
  maxAge: string;
  maxAgeUnit: AgeUnit;
  maxEntries: string;
}

interface GeneralSettings {
  swVersion: string;
  precacheUrls: string;
  offlineFallbackUrl: string;
}

